#!/usr/bin/env python3
"""
format-scan-results.py
读取 scan-results/_combined.json，生成结构化 Markdown 报告，
并自动更新 SKILL.md 的「页面元素速查表」和「模块结构明细」章节。

用法:
  python3 format-scan-results.py [--update-skill] [--skill-path PATH]

不传 --update-skill 时仅输出 Markdown 到 stdout。
"""

import json
import os
import sys
import re
from datetime import datetime
from collections import defaultdict

COMBINED_PATH = os.path.join(os.path.dirname(__file__), '..', 'scan-results', '_combined.json')
DEFAULT_SKILL_PATH = os.path.join(os.path.dirname(__file__), '..', 'SKILL.md')

# 模块名称映射
MODULE_NAMES = {
    'myFocus': '首页',
    'paipai': 'PaiPai',
    'ai-workbench': 'PaiWork',
    'knowledge-base': '云盘',
    'calendar': '日历',
    'meeting': '会议',
    'report': '研报',
    'convert-meeting': '转记',
    'translate-tool': '翻译',
    'announcement': '公告',
    'social-media': '社媒',
}

# 类型中文映射
TYPE_LABELS = {
    'interactive:button': '按钮',
    'interactive:input': '输入框',
    'interactive:editor': '编辑器',
    'interactive:tab': '标签页',
    'interactive:nav': '导航项',
    'interactive:clickable': '可点击区域',
    'content:card': '卡片',
    'content:title': '标题',
    'content:text': '文本',
    'content:icon': '图标',
    'content:image': '图片',
    'content:table': '表格',
    'content:paragraph': '段落',
    'content:listitem': '列表项',
    'container': '容器',
}


def load_combined():
    if not os.path.exists(COMBINED_PATH):
        print(f"错误: 找不到 {COMBINED_PATH}", file=sys.stderr)
        print("请先运行 scan-all-modules.sh", file=sys.stderr)
        sys.exit(1)
    with open(COMBINED_PATH, 'r', encoding='utf-8') as f:
        return json.load(f)


def truncate(text, length=40):
    if not text:
        return ''
    text = text.replace('\n', ' ').strip()
    return text[:length] + ('...' if len(text) > length else '')


def format_selector(sel):
    """格式化选择器，加反引号"""
    if not sel:
        return '`(无)`'
    return f'`{sel}`'


def generate_markdown(data):
    """生成完整的 Markdown 报告"""
    lines = []
    scan_time = datetime.now().strftime('%Y-%m-%d %H:%M')

    lines.append(f'<!-- Auto-generated by format-scan-results.py at {scan_time} -->')
    lines.append(f'<!-- Source: scan-results/_combined.json -->')
    lines.append('')

    for mod_id, mod_data in data.items():
        mod_name = MODULE_NAMES.get(mod_id, mod_id)
        sections = mod_data.get('sections', [])
        nav_items = mod_data.get('navItems', [])

        if not sections:
            continue

        lines.append(f'### {mod_name}（#{mod_id}）')
        lines.append('')

        # 导航项
        if nav_items:
            lines.append('**导航项：**')
            for nav in nav_items[:15]:
                active = ' ← 当前' if nav.get('active') else ''
                text = nav.get('text', '').strip()
                if text:
                    lines.append(f'- `#{nav["id"]}` — {text}{active}')
            lines.append('')

        # 板块明细
        for section in sections:
            sec_name = section.get('name', '未知')
            sec_sel = section.get('selector', '')
            sec_text = truncate(section.get('text', ''), 60)
            nodes = section.get('nodes', [])

            if not nodes:
                continue

            lines.append(f'#### {sec_name}')
            lines.append(f'> 容器: {format_selector(sec_sel)}')
            if sec_text:
                lines.append(f'> 内容: {sec_text}')
            lines.append('')

            # 按类型分组
            by_type = defaultdict(list)
            for node in nodes:
                by_type[node.get('type', 'container')].append(node)

            # 按交互/内容排序
            type_order = [
                'interactive:button', 'interactive:input', 'interactive:editor',
                'interactive:tab', 'interactive:nav', 'interactive:clickable',
                'content:title', 'content:card', 'content:text',
                'content:image', 'content:icon', 'content:table',
                'content:paragraph', 'content:listitem', 'container'
            ]

            for ntype in type_order:
                if ntype not in by_type:
                    continue
                type_label = TYPE_LABELS.get(ntype, ntype)
                group_nodes = by_type[ntype]

                lines.append(f'**{type_label}（{len(group_nodes)}个）**')
                lines.append('')
                lines.append('| 选择器 | 文本/标签 | 尺寸 | 有效 |')
                lines.append('|--------|----------|------|------|')

                for node in group_nodes:
                    sel = format_selector(node.get('selector', ''))
                    text = truncate(node.get('text', ''), 50)
                    rect = node.get('rect', {})
                    size = f'{rect.get("w", 0)}×{rect.get("h", 0)}'
                    valid = '✓' if node.get('validSelector') else '⚠'
                    lines.append(f'| {sel} | {text} | {size} | {valid} |')

                lines.append('')

            lines.append('---')
            lines.append('')

    return '\n'.join(lines)


def generate_quickref_table(data):
    """生成速查表格式"""
    lines = []
    lines.append('| 模块 | 板块 | 元素类型 | 选择器 | 文本 |')
    lines.append('|------|------|---------|--------|------|')

    for mod_id, mod_data in data.items():
        mod_name = MODULE_NAMES.get(mod_id, mod_id)
        sections = mod_data.get('sections', [])

        for section in sections:
            sec_name = section.get('name', '')
            nodes = section.get('nodes', [])

            # 每个板块只取前5个有意义的节点
            shown = 0
            for node in nodes:
                if node.get('type', '').startswith('container'):
                    continue
                if not node.get('validSelector'):
                    continue
                ntype = TYPE_LABELS.get(node['type'], node['type'])
                sel = node.get('selector', '')
                text = truncate(node.get('text', ''), 30)
                lines.append(f'| {mod_name} | {sec_name} | {ntype} | `{sel}` | {text} |')
                shown += 1
                if shown >= 5:
                    break

    return '\n'.join(lines)


def update_skill_md(skill_path, detailed_md, quickref_md):
    """更新 SKILL.md 中的自动扫描章节"""
    if not os.path.exists(skill_path):
        print(f"警告: SKILL.md 不存在于 {skill_path}", file=sys.stderr)
        return

    with open(skill_path, 'r', encoding='utf-8') as f:
        content = f.read()

    # 定义章节标记
    detail_start = '<!-- AUTO_SCAN_DETAIL_START -->'
    detail_end = '<!-- AUTO_SCAN_DETAIL_END -->'
    quickref_start = '<!-- AUTO_SCAN_QUICKREF_START -->'
    quickref_end = '<!-- AUTO_SCAN_QUICKREF_END -->'

    # 构建新内容
    detail_block = f'{detail_start}\n{detailed_md}\n{detail_end}'
    quickref_block = f'{quickref_start}\n{quickref_md}\n{quickref_end}'

    # 如果标记已存在，替换；否则追加到末尾
    if detail_start in content and detail_end in content:
        pattern = re.compile(
            re.escape(detail_start) + r'.*?' + re.escape(detail_end),
            re.DOTALL
        )
        content = pattern.sub(detail_block, content)
    else:
        content += '\n\n---\n\n## 附录：自动扫描结果（页面结构明细）\n\n' + detail_block + '\n'

    if quickref_start in content and quickref_end in content:
        pattern = re.compile(
            re.escape(quickref_start) + r'.*?' + re.escape(quickref_end),
            re.DOTALL
        )
        content = pattern.sub(quickref_block, content)
    else:
        content += '\n\n## 附录：自动扫描速查表\n\n' + quickref_block + '\n'

    with open(skill_path, 'w', encoding='utf-8') as f:
        f.write(content)

    print(f'✓ SKILL.md 已更新: {skill_path}')


def main():
    import argparse
    parser = argparse.ArgumentParser(description='格式化 DOM 扫描结果')
    parser.add_argument('--update-skill', action='store_true', help='更新 SKILL.md')
    parser.add_argument('--skill-path', default=DEFAULT_SKILL_PATH, help='SKILL.md 路径')
    parser.add_argument('--detailed-only', action='store_true', help='仅输出详细报告')
    parser.add_argument('--quickref-only', action='store_true', help='仅输出速查表')
    args = parser.parse_args()

    data = load_combined()

    detailed_md = generate_markdown(data)
    quickref_md = generate_quickref_table(data)

    if args.update_skill:
        update_skill_md(args.skill_path, detailed_md, quickref_md)
    elif args.detailed_only:
        print(detailed_md)
    elif args.quickref_only:
        print(quickref_md)
    else:
        print('=== 详细报告 ===')
        print(detailed_md)
        print('\n\n=== 速查表 ===')
        print(quickref_md)


if __name__ == '__main__':
    main()
